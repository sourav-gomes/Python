# Arithmetic Operarors
print("The value of 3+4 is", 3+4)
print("The value of 3-4 is", 3-4)
print("The value of 3*4 is", 3*4)
print("The value of 3/4 is", 3/4)

# Assignment Operarors
a = 44
a += 10
a -= 20
a *= 11
a /= 10
print("The value of 'a' is", a)


# Comparison Operators -- returns boolean (True/False)

b = (25>67)     # Will return false as it is not true
b1 = (25<67)     # Will return true as it is true
b2 = (25>=67)     # Will return false as it is neither > nor = 
b3 = (25==67)     # Will return false as 25 is not equal to 67
b4 = (67==67)     # Will return true as 25 equal to 67
b5 = (25!=67)     # Will return true as 25 is not equal to 67

print(b)
print(b1)
print(b2)
print(b3)
print(b4)
print(b5)

# Logical Operators -- And, Or, Not

bool1 = True
bool2=False
print("The value of bool1 AND bool2 is:", (bool1 and bool2))    # will return false as both conditions have to fulfil i.e. AND 
print("The value of bool1 OR bool2 is:", (bool1 or bool2))    # will return true as ANY 1 condition have to fulfil i.e. OR
print("The value of NOT bool1 is:", (not bool1))    # will return opposite of bool1 i.e. NOT 









